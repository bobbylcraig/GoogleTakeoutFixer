// TestApplyMetadata_RoundTrip writes metadata into a real JPEG via exiftool and
// reads it back. Skipped automatically when exiftool is unavailable.
func TestApplyMetadata_RoundTrip(t *testing.T) {
	if _, err := exec.LookPath("exiftool"); err != nil {
		t.Skip("exiftool not installed; skipping round-trip test")
	}

	dir := t.TempDir()
	img := filepath.Join(dir, "photo.jpg")
	if err := writeBytes(img, tinyJPEG); err != nil {
		t.Fatal(err)
	}

	if err := InitializeExifTool(); err != nil {
		t.Fatalf("init exiftool: %v", err)
	}
	defer CloseExifTool()

	m := baseMeta()
	m.Title = "Beach"
	m.Description = "beach day"
	m.Favorited = true
	m.People = []struct {
		Name string `json:"name"`
	}{{Name: "Alice"}, {Name: "Bob"}}
	m.GeoData.Latitude = 40.5
	m.GeoData.Longitude = -3.7

	if err := ApplyMetadata(img, m); err != nil {
		t.Fatalf("ApplyMetadata: %v", err)
	}

	// Read tags back.
	out, err := exec.Command("exiftool",
		"-s3",
		"-XMP:PersonInImage",
		"-XMP:Rating",
		"-IPTC:Keywords",
		"-XMP-dc:Subject",
		"-GPSLatitude",
		"-ImageDescription",
		img,
	).CombinedOutput()
	if err != nil {
		t.Fatalf("exiftool read: %v\n%s", err, out)
	}
	got := string(out)
	t.Logf("exiftool output:\n%s", got)

	for _, want := range []string{"Alice", "Bob", "beach day"} {
		if !strings.Contains(got, want) {
			t.Errorf("expected %q in written metadata, got:\n%s", want, got)
		}
	}
	if !strings.Contains(got, "5") {
		t.Errorf("expected Rating 5 in metadata, got:\n%s", got)
	}
}

